#permimeter of a circle
radius=int(input("the radius of the circle is : "))
pi=3.14
perimeter=2 * pi * radius
print(int(perimeter))
print("the perimeter of the circle with radius {} and pi {} is {}".format(float(radius),float(pi),float(perimeter)))


